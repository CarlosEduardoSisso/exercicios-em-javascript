// JavaScript Bitwise Operators

let a = 5 & 1; // and
let b = 5 | 1; // or
let c = ~ 5; // not
let d = 5 ^ 1; // xor
let e = 5 << 1; // left shift
let f = 5 >> 1; // right shift
let g = 5 >>> 1; // unsigned right shift

console.log(a);
console.log(b);
console.log(c);
console.log(d);
console.log(e);
console.log(f);
console.log(g);
