//JavaScript Assignment Operators

//Exemplo 1
let a = 5;
let b = a;
console.log(b);

//Exemplo 2
let c = 10;
c += 5; // o mesmo que x = x + 5
console.log(c);

//Exemplo 3
let d = 10;
d -= 8; // o mesmo que c = c - 8
console.log(d);

//Exemplo 4
let e = 8;
e *= 12 // o mesmo que d = d * 12
console.log(e);

//Exemplo 5
let f = 145;
f /= 5; // o mesmo que e = e / 5
console.log(f);

//Exemplo 6
let g = 395;
g %= 8; // Módulo (Resto da Divisão). É o mesmo que f = f % 8
console.log(g);

//Exemplo 7
let h = 7;
h **= 3;
console.log(h);